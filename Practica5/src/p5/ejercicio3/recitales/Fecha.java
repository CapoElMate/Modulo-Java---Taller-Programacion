
package p5.ejercicio3.recitales;


public class Fecha {
    private String ciudad;
    private String dia;
    
    public Fecha(String ciudad,String dia){
        this.ciudad = ciudad;
        this.dia = dia;
    }

    public String getCiudad() {
        return ciudad;
    }

    public String getDia() {
        return dia;
    }
    
    
    
}
